from coverai.domain.entities import Employer, ResumeProfile, Vacancy
from coverai.domain.enums import Tone
from coverai.services.prompts.few_shot_examples import few_shot_lines
from coverai.services.prompts.text_normalizer import normalize_text
from coverai.services.prompts.vacancy_formatter import vacancy_detail_lines


def build_cover_letter_prompt(
    profile: ResumeProfile,
    vacancy: Vacancy,
    employer: Employer,
    tone: Tone,
) -> str:
    """Собирает prompt письма."""
    vacancy_url = vacancy.url or "not provided"

    lines = [
        "Напиши готовое сопроводительное письмо на русском.",
        "Цель: показать работодателю, где кандидат будет полезен в работе.",
        "Формат: 2-3 коротких абзаца, до 130 слов, plain text.",
        "Без Markdown, списков, заголовков, подписи, контактов и плейсхолдеров.",
        "Начни письмо с приветствия: 'Добрый день.' или 'Здравствуйте.'. "
        "Если текст вакансии неформальный и содержит 'привет', "
        "можно начать с 'Привет.'",
        "Пиши естественно и грамотно, без канцелярита и сплошного списка стека.",
        "Творчески работай только с подачей, не с фактами.",
        "Пиши от первого лица, но не упоминай резюме как документ.",
        "В первом абзаце упомяни название вакансии и работодателя.",
        "Не пиши 'вашей компании', если известно название работодателя.",
        "Не пересказывай резюме. Пиши как рабочий отклик на конкретную вакансию.",
        "Не пиши мета-фразы: 'в описании вакансии', 'в вакансии указано', "
        "'из резюме видно', 'мой профиль'.",
        "Не копируй пункты из резюме или вакансии механически.",
        "Не перечисляй стек или задачи через запятую как 'готов делать A, B, C'.",
        "Не перечисляй задачи или стек; перефразируй их как рабочую ситуацию.",
        "Связывай опыт и задачи в 1-2 осмысленных рабочих сценария.",
        "Если по вакансии понятен продукт или компания, можно коротко объяснить, "
        "почему кандидату интересны именно они.",
        "Мотивация к компании должна быть конкретной и спокойной, без лести.",
        "Для мотивации называй конкретику из вакансии: продукт, домен, аудиторию, "
        "тип клиентов, технологическую задачу или название работодателя.",
        "Не пиши общие фразы вроде 'ваш продукт', 'направление компании' или "
        "'мне интересна ваша компания' без конкретного продолжения.",
        "Если конкретики про продукт или компанию нет, лучше пропусти мотивацию "
        "к компании.",
        "Если из профиля или контекста явно понятно, что кандидат знаком с продуктом "
        "или сам им пользуется, можно упомянуть это как мотивацию.",
        "Не выдумывай знакомство с продуктом, личное использование или давний интерес.",
        "Пиши так, будто кандидат уже понял контекст компании и отвечает человеку, "
        "а не пересказывает источники.",
        "Не делай шаблонное письмо. Меняй начало, ритм и финальную фразу.",
        "Не повторяй структуру примеров: они показывают диапазон подачи, а не "
        "шаблон абзацев.",
        "Слово 'готов' допустимо, если звучит естественно, но не повторяй его.",
        "Не строй каждый абзац по шаблону 'готов + действие'.",
        "Не используй клише: 'буду рад', 'меня заинтересовала', 'интересен мне "
        "именно как продукт', 'не абстрактно'.",
        "Избегай парных шаблонных оборотов, где одна мысль усиливается через "
        "отрицание и добавление второй части.",
        "Собери письмо вокруг 1-2 сильных задач из вакансии.",
        "Для каждой задачи покажи: что нужно работодателю, как кандидат поможет, "
        "какой опыт это подтверждает.",
        "Используй живые глаголы: разрабатывать, дорабатывать, интегрировать, "
        "тестировать, поддерживать, улучшать, разбираться, запускать.",
        "Финал должен быть уверенным call to action: предложи обсудить контекст, "
        "первые задачи, ожидания от роли, следующий шаг или формат интервью.",
        "Не предлагай готовые решения до знакомства со спецификой проекта; лучше "
        "покажи готовность разобраться, задать правильные вопросы и быстро "
        "включиться в работу.",
        "Формулируй финал живо и разнообразно: можно написать, что кандидат "
        "готов пообщаться на интервью и показать, чем будет полезен.",
        "Не заканчивай каждое письмо фразой 'Готов обсудить...'.",
        "Не добавляй факты о кандидате, которых нет в резюме.",
        "Не заявляй достижений, метрик или оптимизаций, если их нет в резюме.",
        "Не приписывай кандидату требования, обязанности или годы опыта из вакансии.",
        f"Tone: {tone.value}.",
        "",
        "Примеры хорошего стиля:",
        "Не копируй из примеров факты, компании или технологии; "
        "используй только подачу.",
        "Названия компаний и продуктов в примерах условные; не копируй их.",
        *few_shot_lines(),
        "",
        "Резюме:",
        normalize_text(profile.resume_text),
        "",
        "Вакансия:",
        f"Название: {vacancy.title}",
        f"Работодатель: {employer.name}",
        f"URL: {vacancy_url}",
    ]
    lines.extend(vacancy_detail_lines(vacancy))
    return "\n".join(lines)
